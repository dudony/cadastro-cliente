    private void BsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsalvarActionPerformed
        if (this.Codigo.getText().length() > 0) {
            u.setCodigo(Integer.parseInt(this.Codigo.getText()));
        }else{
            u.setCodigo(1);
        }
        u.setNome(this.Nome.getText());
        u.setLogin(this.Login.getText());
        u.setSenha(this.Senha.getText());
          
          JOptionPane.showMessageDialog(null, u.getNome() + " seu login Ã© " + u.getLogin());
    }//GEN-LAST:event_BsalvarActionPerformed

